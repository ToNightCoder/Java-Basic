/*Multiple Parameters
You can have as many parameters as you like:*/
public class MethodMultipleParameters {
    
    static void myMethod(String fname, int age) {
        System.out.println(fname + " is " + age);
      }
    
      public static void main(String[] args) {
        myMethod("Liam", 5);
        myMethod("Jenny", 8);
        myMethod("Anja", 31);
      }
    }
    

    
